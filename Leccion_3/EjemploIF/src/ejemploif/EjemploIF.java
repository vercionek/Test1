package ejemploif;

public class EjemploIF {

	public static void main(String[] args) {
		//Ejemplo IF
		
		int x = 20;
		
		if ( x == 20) {
			System.out.println("X es igual a 20");
		}
		else if (x == 30) {
			System.out.println("X es igual a 30");
		}
		else if (x == 40) {
			System.out.println("X es igual a 40");
		}
		else {
			System.out.println("X no es ni 20 ni 30 ni 40");
		}

	}

}
